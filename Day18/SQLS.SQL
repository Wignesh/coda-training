CREATE PROCEDURE PROCEDURE1()
BEGIN
    UPDATE
        USERS
    SET
        FLAG = 0;
END

CREATE PROCEDURE PROCEDURE2(IN NAME VARCHAR(30),IN FL INT)
BEGIN
    UPDATE
        USERS
    SET
        FLAG = FL
    WHERE
        UNAME = NAME;
END

CREATE PROCEDURE PROCEDURE3(IN NAME VARCHAR(30),OUT FL INT)
BEGIN
    SELECT
        FLAG
    INTO
        FL
    FROM
        USERS
    WHERE
        UNAME = NAME;
END